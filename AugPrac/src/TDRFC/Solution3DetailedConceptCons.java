package TDRFC;

public class Solution3DetailedConceptCons {
	int a,b,c,d,e;
	public Solution3DetailedConceptCons(int a1, int b1, int c1, int d1, int e1) {
		a=a1;
		b=b1;
		c=c1;
		d=d1;
		e=e1;
	}

	public static void main(String[] args) {
		Solution3DetailedConceptCons ob= new Solution3DetailedConceptCons(5,6,7,8,9);	
		System.out.println(ob.a);
		System.out.println(ob.b);
		System.out.println(ob.c);
		System.out.println(ob.d);
		System.out.println(ob.e);

	}

}
